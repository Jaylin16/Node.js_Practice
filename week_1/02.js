// 데이터 타입

// 1. 숫자
// 1-1. 정수
let num = 10;
console.log(num);
console.log(typeof num);

// 1-2. 실수(float)
let num2 = 3.14;
console.log(num2);
console.log(typeof num2);

// 1-3. 지수형(Exp)
let num3 = 2.5e5; // 2.5 x 10^5
console.log(num3);
console.log(typeof num3);

// 1-4. Nan : Not a Number
let num4 = "hello" / 2;
console.log(num4);
console.log(typeof num4);

// 1-5. Infinity(무한대)
let num5 = 1 / 0;
console.log(num5);
console.log(typeof num5);

// 1-6. Infinity(무한대)
let num6 = -1 / 0;
console.log(num6);
console.log(typeof num6);