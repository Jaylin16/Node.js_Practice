// 불리언(Boolean)
let bool1 = true;
let bool2 = false;

console.log(bool1);
console.log(typeof bool1);
console.log(bool2);
console.log(typeof bool2);

// undefined : 정의되지 않은 값. (즉, 값이 없음.)
let x;
console.log(x);

// null : 값이 존재하지 않음을 '명시적'으로 나타낸 것. (즉, null이란 값이 있음.)
let y = null;
console.log(y);

